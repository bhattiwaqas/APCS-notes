public class Variable3 {
  public static void main (String[] args) {
    int age = 15;
    int grade = 11;
    String name = "Waqas";
    String initial = "I";
    System.out.println("My name is " + name);
    System.out.println("I am " + age + " years old");
    System.out.println("I am in the " + grade + "th grade");
    System.out.println("My middle intial is " + initial);
    
    int num = 3;
    System.out.println("The magic number is " + num);
    num = 3 * 4;
    System.out.println("The magic number is " + num);
    num = num + 6; // num starts at 12... 12 + 6
    System.out.println("The magic number is " + num);
    num = num + 1;
    System.out.println("The magic number is " + num);
    num = num - 13;
    System.out.println("The magic number is " + num);
       
  }
}